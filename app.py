# --- 1. 動態串接 OpenStreetMap (Overpass API) 查詢腳本 ---
@st.cache_data(ttl=86400)  # 快取 24 小時
def fetch_live_amenities(lat, lon, radius=3000):
    overpass_url = "https://overpass-api.de/api/interpreter"
    overpass_query = f"""
    [out:json][timeout:15];
    (
      node["shop"="convenience"](around:{radius},{lat},{lon});
      node["shop"="supermarket"](around:{radius},{lat},{lon});
      node["amenity"="fast_food"](around:{radius},{lat},{lon});
      node["shop"="department_store"](around:{radius},{lat},{lon});
      node["amenity"="marketplace"](around:{radius},{lat},{lon});
      node["amenity"="post_office"](around:{radius},{lat},{lon});
      node["bank"](around:{radius},{lat},{lon});
      node["amenity"="bank"](around:{radius},{lat},{lon});
      node["leisure"="park"](around:{radius},{lat},{lon});
      node["leisure"="pitch"](around:{radius},{lat},{lon});
    );
    out body;
    """
    try:
        response = requests.post(overpass_url, data={'data': overpass_query}, timeout=10)
        if response.status_code == 200:
            data = response.json()
            elements = data.get('elements', [])
            
            if len(elements) > 0:
                counts = {
                    "convenience": 0, "supermarket": 0, "fast_food": 0, 
                    "dept": 0, "market": 0, "post_bank": 0, "park_sport": 0
                }
                for elem in elements:
                    tags = elem.get('tags', {})
                    if tags.get('shop') == 'convenience': counts["convenience"] += 1
                    elif tags.get('shop') == 'supermarket': counts["supermarket"] += 1
                    elif tags.get('amenity') == 'fast_food': counts["fast_food"] += 1
                    elif tags.get('shop') == 'department_store': counts["dept"] += 1
                    elif tags.get('amenity') == 'marketplace': counts["market"] += 1
                    elif tags.get('amenity') in ['post_office', 'bank'] or 'bank' in tags: counts["post_bank"] += 1
                    elif tags.get('leisure') in ['park', 'pitch']: counts["park_sport"] += 1
                return counts
        return None
    except Exception:
        return None

